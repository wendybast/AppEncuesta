    private void GuardarButtomActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_GuardarButtomActionPerformed

        Validaciones validar = new Validaciones();

        if (validar.vacio(nombre.getText())) {
            if (validar.vacio(apellido.getText())) {
                if (validar.esMayor(numCliente.getText())) {

                    cliente persona1 = new cliente(nombre.getText(), apellido.getText(), numCliente.getText());

                    encuesta enc1 = new encuesta(nAtencion.toString(), nResolucion.toString(), nRecomendacion.toString());

                    int ate = Integer.parseInt(nAtencion.getSelectedItem().toString());
                    int res = Integer.parseInt(nResolucion.getSelectedItem().toString());
                    int rec = Integer.parseInt(nRecomendacion.getSelectedItem().toString());

                    Promedio pro = new Promedio();
                    pro.proEncu(ate, res, rec);

                    pro.setVisible(true);
                    this.dispose();
                } else {
                    JOptionPane.showMessageDialog(null, "INGRESE EL NUMERO DE CLIENTE", "WARNING_MESSAGE", JOptionPane.WARNING_MESSAGE);
                }
            } else {
                JOptionPane.showMessageDialog(null, "INGRESE SU APELLIDO", "WARNING_MESSAGE", JOptionPane.WARNING_MESSAGE);
            }
     
        }else{
            JOptionPane.showMessageDialog(null, "INGRESE SU NOMBRE", "WARNING_MESSAGE", JOptionPane.WARNING_MESSAGE);
        }     
    }//GEN-LAST:event_GuardarButtomActionPerformed
    
